  </header>

  <main class="px-1 ps-3 card color mx-1">
    <section class="">
      <p>Supro web sitesini ziyaret ettiğinizde, kullanıcı deneyimini geliştirmek, siteyi güvenli ve verimli bir şekilde sunmak amacıyla çerezler (cookies) kullanılmaktadır.</p>
    </section>

    <section>
      <h3>1) Çerez Nedir?</h3>
      <div class="">
        <p>Çerez, bir web sitesi tarafından tarayıcınıza gönderilen ve tarayıcınızda saklanan küçük metin dosyasıdır. Bu dosya, web sitesinin sizi tanımasına, tercihlerinizi hatırlamasına ve site kullanımınızı analiz etmesine yardımcı olur.</p>
      </div>
    </section>

    <section>
      <h3>2) Kullanılan Çerez Türleri</h3>
      <div class="">
        <ul>
          <li><strong>Zorunlu Çerezler:</strong> Siteyi çalıştırmak ve temel işlevlerini sağlamak için kullanılır.</li>
          <li><strong>Analitik Çerezler:</strong> Site trafiğini ve kullanım verilerini analiz ederek performans iyileştirmesi yapılmasını sağlar.</li>
          <li><strong>Fonksiyonel Çerezler:</strong> Kullanıcı tercihlerini hatırlamak ve daha kişiselleştirilmiş bir deneyim sunmak için kullanılır.</li>
          <li><strong>Pazarlama / Reklam Çerezleri:</strong> İlgi alanınıza uygun içerik ve reklam sunmak amacıyla kullanılır.</li>
        </ul>
      </div>
    </section>

    <section>
      <h3>3) Çerezlerin Kullanım Amaçları</h3>
      <div class="">
        <ul>
          <li>Site performansını ve kullanıcı deneyimini artırmak</li>
          <li>Kullanıcı tercihlerini hatırlamak</li>
          <li>Güvenlik ve site bütünlüğünü sağlamak</li>
          <li>Analitik veri toplamak ve raporlamak</li>
          <li>Reklam ve pazarlama stratejilerini geliştirmek</li>
        </ul>
      </div>
    </section>

    <section>
      <h3>4) Çerez Yönetimi</h3>
      <div class="">
        <p>Tarayıcınız üzerinden çerezleri kabul edebilir, reddedebilir veya silebilirsiniz. Ancak bazı çerezlerin devre dışı bırakılması site işlevselliğini etkileyebilir.</p>
        <p>Örnek tarayıcı ayarları:</p>
        <ul>
          <li>Chrome: Ayarlar → Gizlilik ve güvenlik → Çerezler ve diğer site verileri</li>
          <li>Firefox: Seçenekler → Gizlilik ve güvenlik → Çerezler ve site verileri</li>
          <li>Safari: Tercihler → Gizlilik → Çerezleri engelle veya sil</li>
        </ul>
      </div>
    </section>

    <section>
      <h3>5) Üçüncü Taraf Çerezleri</h3>
      <div class="">
        <p>Site üzerinde kullanılan bazı çerezler üçüncü taraf hizmet sağlayıcılar (ör. Google Analytics, sosyal medya widget’ları) tarafından oluşturulabilir. Bu çerezlerin yönetimi ve gizlilik politikaları, ilgili üçüncü tarafın sorumluluğundadır.</p>
      </div>
    </section>

    <section>
      <h3>6) Güncellemeler</h3>
      <div class="">
        <p>Çerez Politikası, mevzuat veya hizmetlerdeki değişikliklere göre güncellenebilir. Güncel politika her zaman bu sayfada yayımlanır.</p>
      </div>
    </section>
