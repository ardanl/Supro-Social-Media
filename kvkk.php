    <section>
      <h3>3) İşleme Amaçları</h3>
      <ul class="">
        <li>Üyelik oluşturma, kimlik doğrulama ve hesap yönetimi</li>
        <li>Hizmetin sunulması, bakım ve iyileştirme, hata/performans takibi</li>
        <li>Güvenliğin sağlanması, kötüye kullanımın önlenmesi ve loglama</li>
        <li>Bildirim ve iletişimlerin yürütülmesi (onayınıza bağlı ticari iletiler dahil)</li>
        <li>Yasal yükümlülüklerin yerine getirilmesi ve uyuşmazlıkların yönetimi</li>
        <li>Analitik ve raporlama; ürün geliştirme ve kişiselleştirme</li>
      </ul>
    </section>

    <section>
      <h3>4) Toplama Yöntemi ve Hukuki Sebep</h3>
      <div class="">
        <p>Verileriniz; web formları, üyelik işlemleri, çerezler, mobil/web uygulama kullanımı, destek talepleri ve üçüncü taraf hizmetler (barındırma, e-posta, ödeme vb.) aracılığıyla otomatik/yarı otomatik yollarla toplanır.</p>
        <h3>KVKK Dayanakları</h3>
        <ul>
          <li><strong>5/2-c:</strong> Sözleşmenin kurulması/ifası (üyelik ve hizmet sunumu)</li>
          <li><strong>5/2-ç:</strong> Veri sorumlusunun hukuki yükümlülüğü</li>
          <li><strong>5/2-f:</strong> Meşru menfaat (güvenlik, hata önleme, analitik)</li>
          <li><strong>5/1:</strong> Açık rıza (pazarlama, opsiyonel çerezler gibi haller)</li>
        </ul>
      </div>
    </section>

    <section>
      <h3>5) Aktarımlar (Yurt İçi / Yurt Dışı)</h3>
      <div class="">
        <p>Hizmetten yararlanmanız için zorunlu hallerde verileriniz; barındırma, e-posta, sms, analitik, güvenlik, yedekleme, ödeme ve bildirim altyapısı sağlayıcılarıyla paylaşılabilir. Sunucuların/sağlayıcıların yurt dışında bulunması halinde, KVKK ve ikincil mevzuata uygun aktarım mekanizmaları uygulanır.</p>
        <p><strong>Alıcı grupları örnekleri:</strong> Bilişim altyapı sağlayıcıları, hukuk/muhasebe danışmanları, yetkili kamu kurumları ve mahkemeler (gerekli hallerde).</p>
      </div>
    </section>

    <section>
      <h3>6) Saklama Süreleri</h3>
      <div class="">
        <p>Veriler, ilgili mevzuatta öngörülen azami süreler ve şirket içi saklama politikalarına uygun olarak; amaç ortadan kalktığında veya yasal süre dolduğunda silinir, yok edilir ya da anonim hale getirilir.</p>
      </div>
    </section>

    <section>
      <h3>7) KVKK 11. Madde Kapsamındaki Haklarınız</h3>
      <ul class="">
        <li>İşlenip işlenmediğini öğrenme ve bilgi talep etme</li>
        <li>Amacına uygun kullanımı öğrenme</li>
        <li>Yurt içi/yurt dışı aktarılan üçüncü kişileri bilme</li>
        <li>Eksik/yanlış işlenmişse düzeltilmesini isteme</li>
        <li>Silinmesini/yok edilmesini isteme ve yapılan işlemlerin bildirilmesini talep etme</li>
        <li>Otomatik sistemlerce analiz sonucu aleyhe sonuca itiraz</li>
        <li>Zarara uğramanız hâlinde tazminat talep etme</li>
      </ul>
    </section>

